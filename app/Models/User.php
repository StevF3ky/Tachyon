<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Foundation\Auth\User as Authenticatable;
use Illuminate\Notifications\Notifiable;
use Illuminate\Database\Eloquent\Relations\BelongsToMany; // <--- WAJIB ADA

class User extends Authenticatable
{
    use HasFactory, Notifiable;

    protected $fillable = [
        'name',
        'email',
        'password',
    ];

    protected $hidden = [
        'password',
        'remember_token',
    ];

    protected function casts(): array
    {
        return [
            'email_verified_at' => 'datetime',
            'password' => 'hashed',
        ];
    }

    // --- RELASI PENTING UNTUK LIBRARY ---
    // Fungsi ini menghubungkan User dengan Thread yang disimpan
    public function savedThreads(): BelongsToMany
    {
        return $this->belongsToMany(Thread::class, 'saved_threads')->withTimestamps();
    }
}